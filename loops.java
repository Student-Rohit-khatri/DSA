package com.rohit;

import java.util.*;

public class loops {
    public static void main(String[] args) {
        System.out.println("Enter a number : ");
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();

        for(int i=0 ; i<=a;i++){
            System.out.print( i );
        }
    }
}
